/*
A literal is a Literal representation of a value on JS. It is a fixed value - not a variable - they are literally provided in the code. 'hard coded values'

A variable is not a literal, the info assigned to the variable is the literal 
Example:

let         placeholder = 'text';
(keyword)   (variable)     (value/literal)

*/ 

// STRING LITERAL
let city = 'Indianapolis'; 

// NUMERIC LITERAL
let num = 123;  

// BOOLEAN LITERAL
let isOn = true;

// OBJECT LITERAL 
let movie = {
    title: 'Elf',
    rating: 'PG',
    release: 'Nov 7th 2003'
}; 

// ARRAY LITERAL 
let daysOfWeek = ['sun', 'mon', 'tues', 'wed', 'thurs', 'fri', 'sat'];

